package CommandPractice;

public class Radio {
    public void turnOn() {
        System.out.println("Radio on!");
    }

    public void turnOff() {
        System.out.println("Radio off!");
    }

    public void volumeUp() {
        System.out.println("Radio volume up!");
    }

    public void volumeDown() {
        System.out.println("Radio volume down!");
    }
}